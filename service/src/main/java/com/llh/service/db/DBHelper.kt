class DBHelper(context: Context) : SQLiteOpenHelper(context, "appHelper.db", null, 1) {

    override fun onCreate(db: SQLiteDatabase) {
        db.execSQL("CREATE TABLE IF NOT EXISTS $TABLE_NAME($KEY_PRIMARY_ID INTEGER, $KEY_BASE_URL TEXT)")
    }

    override fun onUpgrade(db: SQLiteDatabase, oldVersion: Int, newVersion: Int) {

    }

    companion object {
        const val TABLE_NAME = "appurl"
        const val KEY_PRIMARY_ID = "_id"
        const val KEY_BASE_URL = "baseUrl"
        const val DEFAULT_ID_VALUE = "1"
        const val DEFAULT_URL_VALUE = APP_URL_Q
    }
}