object AppHelper {
    var mServiceContent = ""
    private val uriApp: Uri = Uri.parse("content://com.llh.service.apphelper/appurl")
    fun queryData(mContext: Context) {
        val cursor = mContext.contentResolver.query(uriApp, arrayOf("_id", "baseUrl"), "_id=?", Array(1) { "1" }, null)
        cursor?.apply {
            if (moveToNext()) {
                mServiceContent = StringBuilder().append("通过服务器查询的数据:\n")
                    .append("${getColumnName(0)}=${getString(0)},\n")
                    .append("${getColumnName(1)}=${getString(1)}").toString()
            }
        }
        cursor?.close()
    }
}